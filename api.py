"""
api.py
------
Flask REST API that serves Data Warehouse analytics data to the React frontend.
Endpoints return JSON data from PostgreSQL star schema AND snowflake schema queries.

Endpoints:
    ── Star Schema ──
    GET /api/schema              → Star schema table info (columns, row counts)
    GET /api/etl-log             → ETL pipeline log contents
    GET /api/sample/<table>      → First 10 rows of a star schema table
    GET /api/sales-by-country    → Total sales aggregated by country
    GET /api/top-products        → Top 5 products by revenue
    GET /api/monthly-revenue     → Monthly revenue trend
    GET /api/top-customers       → Top 10 customers by spending
    GET /api/quarterly-category  → Quarterly revenue by product category

    ── Snowflake Schema ──
    GET /api/snowflake-schema    → Snowflake schema table info (columns, row counts)
    GET /api/snowflake-sample/<table> → First 10 rows of a snowflake table
    GET /api/snowflake-sales-by-country   → Sales by country (using normalized tables)
    GET /api/snowflake-top-products       → Top products (using normalized tables)
    GET /api/snowflake-comparison         → Star vs Snowflake comparison data

Usage:
    python3 api.py
"""

from flask import Flask, jsonify
from flask_cors import CORS
from sqlalchemy import create_engine, text
from config import DATABASE_URL

app = Flask(__name__)
CORS(app)

engine = create_engine(DATABASE_URL, echo=False)


def query_to_list(sql: str) -> list[dict]:
    """Execute SQL and return list of dicts."""
    with engine.connect() as conn:
        result = conn.execute(text(sql))
        columns = list(result.keys())
        return [dict(zip(columns, row)) for row in result.fetchall()]


