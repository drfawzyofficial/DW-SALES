# SNOWFLAKE SCHEMA ENDPOINTS
# ══════════════════════════════════════════════════════════════════════════════
# The snowflake schema normalizes dimension tables further.
# For example, instead of storing "Electronics" as a string in dim_products,
# the snowflake version stores a category_id that references dim_categories.
#
# Key difference from star schema:
#   Star:      fact_sales → dim_products (has category string)
#   Snowflake: fact_sales_sf → dim_products_sf → dim_categories (category in separate table)
#
# This means snowflake queries need MORE JOINs but have LESS data redundancy.
# ══════════════════════════════════════════════════════════════════════════════

@app.route("/api/snowflake-schema")
def get_snowflake_schema():
    """
    Return snowflake schema table info (columns, row counts).

    Similar to /api/schema but for snowflake tables.
    Notice how dim_products_sf has category_id (FK) instead of category (string),
    and dim_customers_sf has country_id (FK) instead of country (string).
    The sub-dimension tables (dim_categories, dim_countries) hold the actual values.
    """
    tables = {}
    sf_tables = [
        "dim_categories",      # Sub-dimension: product categories
        "dim_countries",       # Sub-dimension: customer countries
        "dim_products_sf",     # Normalized dimension (uses category_id FK)
        "dim_customers_sf",    # Normalized dimension (uses country_id FK)
        "dim_date",            # Shared with star schema (already normalized)
        "fact_sales_sf",       # Fact table (same structure as star)
    ]
    for table in sf_tables:
        cols = query_to_list(f"""
            SELECT column_name, data_type
            FROM information_schema.columns
            WHERE table_name = '{table}'
            ORDER BY ordinal_position
        """)
        count = query_to_list(f"SELECT COUNT(*) as count FROM {table}")[0]["count"]
        tables[table] = {"columns": cols, "row_count": count}
    return jsonify(tables)


@app.route("/api/snowflake-sample/<table_name>")
def get_snowflake_sample(table_name):
    """
    Return first 10 rows of a snowflake schema table.

    For sub-dimension tables (dim_categories, dim_countries), this shows the
    lookup values extracted from the original star schema dimensions.
    For dim_products_sf and dim_customers_sf, you can see the FK references
    instead of the full string values.
    """
    allowed = [
        "dim_categories", "dim_countries",
        "dim_products_sf", "dim_customers_sf",
        "dim_date", "fact_sales_sf",
    ]
    if table_name not in allowed:
        return jsonify({"error": "Invalid table name"}), 400
    rows = query_to_list(f"SELECT * FROM {table_name} LIMIT 10")
    # Convert date objects to strings
    for row in rows:
        for k, v in row.items():
            if hasattr(v, "isoformat"):
                row[k] = v.isoformat()
    return jsonify(rows)


@app.route("/api/snowflake-sales-by-country")
def snowflake_sales_by_country():
    """
    Total sales by country using SNOWFLAKE schema tables.

    SQL Explanation (notice the extra JOIN compared to star schema):
    - Star Schema:      fact_sales → dim_customers (country is in dim_customers)
    - Snowflake Schema: fact_sales_sf → dim_customers_sf → dim_countries

    The snowflake version needs an EXTRA JOIN because the country name is
    stored in a separate dim_countries table (normalized), while in star schema
    it's stored directly in dim_customers (denormalized).

    This is the key trade-off:
    - Star: Fewer JOINs = faster queries, but country is duplicated across customers
    - Snowflake: More JOINs = slightly slower, but country is stored only once
    """
    data = query_to_list("""
        SELECT
            co.country_name AS country,
            COUNT(f.order_id)::int AS total_orders,
            SUM(f.quantity)::int AS total_units_sold,
            ROUND(SUM(f.total_amount)::numeric, 2)::float AS total_revenue
        FROM fact_sales_sf f
        JOIN dim_customers_sf c ON f.customer_id = c.customer_id
        JOIN dim_countries co ON c.country_id = co.country_id
        GROUP BY co.country_name
        ORDER BY total_revenue DESC
    """)
    return jsonify(data)


@app.route("/api/snowflake-top-products")
def snowflake_top_products():
    """
    Top 5 products by revenue using SNOWFLAKE schema tables.

    SQL Explanation (notice the extra JOIN compared to star schema):
    - Star Schema:      fact_sales → dim_products (category is in dim_products)
    - Snowflake Schema: fact_sales_sf → dim_products_sf → dim_categories

    To get the category NAME in snowflake, we must join through dim_products_sf
    to dim_categories, because dim_products_sf only stores category_id.
    """
    data = query_to_list("""
        SELECT
            p.product_name,
            cat.category_name AS category,
            COUNT(f.order_id)::int AS times_ordered,
            SUM(f.quantity)::int AS total_units_sold,
            ROUND(SUM(f.total_amount)::numeric, 2)::float AS total_revenue
        FROM fact_sales_sf f
        JOIN dim_products_sf p ON f.product_id = p.product_id
        JOIN dim_categories cat ON p.category_id = cat.category_id
        GROUP BY p.product_name, cat.category_name
        ORDER BY total_revenue DESC
        LIMIT 5
    """)
    return jsonify(data)


@app.route("/api/snowflake-comparison")
def snowflake_comparison():
    """
    Return comparison data between Star and Snowflake schemas.

    This endpoint provides side-by-side metrics so students can understand
    the structural differences:
    - Table counts (star has 4 tables, snowflake has 6)
    - Total columns across all tables
    - Number of JOINs needed for common queries
    - Storage characteristics

    Educational Note:
    Both schemas produce IDENTICAL analytical results. The difference is
    in how the data is organized (normalized vs denormalized) and the
    implications for query complexity and storage efficiency.
    """
    # Star schema metrics
    star_tables = ["dim_customers", "dim_products", "dim_date", "fact_sales"]
    star_info = {}
    total_star_cols = 0
    total_star_rows = 0
    for t in star_tables:
        cols = query_to_list(f"""
            SELECT COUNT(*) as count FROM information_schema.columns
            WHERE table_name = '{t}'
        """)[0]["count"]
        rows = query_to_list(f"SELECT COUNT(*) as count FROM {t}")[0]["count"]
        star_info[t] = {"columns": cols, "rows": rows}
        total_star_cols += cols
        total_star_rows += rows

    # Snowflake schema metrics
    sf_tables = ["dim_categories", "dim_countries", "dim_products_sf",
                 "dim_customers_sf", "dim_date", "fact_sales_sf"]
    sf_info = {}
    total_sf_cols = 0
    total_sf_rows = 0
    for t in sf_tables:
        cols = query_to_list(f"""
            SELECT COUNT(*) as count FROM information_schema.columns
            WHERE table_name = '{t}'
        """)[0]["count"]
        rows = query_to_list(f"SELECT COUNT(*) as count FROM {t}")[0]["count"]
        sf_info[t] = {"columns": cols, "rows": rows}
        total_sf_cols += cols
        total_sf_rows += rows

    return jsonify({
        "star": {
            "tables": star_info,
            "total_tables": len(star_tables),
            "total_columns": total_star_cols,
            "total_rows": total_star_rows,
            "joins_for_country_query": 1,    # fact → dim_customers
            "joins_for_product_query": 1,    # fact → dim_products
        },
        "snowflake": {
            "tables": sf_info,
            "total_tables": len(sf_tables),
            "total_columns": total_sf_cols,
            "total_rows": total_sf_rows,
            "joins_for_country_query": 2,    # fact → dim_customers_sf → dim_countries
            "joins_for_product_query": 2,    # fact → dim_products_sf → dim_categories
        },
    })


